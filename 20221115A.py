# Estructuras de control
# si condicional (if)

# OPERADORES DE COMPARASIÓN
# a==b   a > b    a < b    a>=b    a<=b   a != b

a = 'z'  # 122
b = 'c'  # 99

if a == b:
    print('a y b son iguales') # esta lìnea está identada (tabulación)

if a < b:
    print('a es menor que b') # esta lìnea está identada (tabulación)

if a > b:
    print('a es mayor que b') # esta lìnea está identada (tabulación)



