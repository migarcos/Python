# AMostrar los números de 1 a 10
# Usando el ciclo While
i = 1   # Variable de control 

while i < 11:  # Condición
    print(i)   # Acción a repetir
    i = i + 1

print('hemos terminado el ciclo')
# B mostrar los números del 10 al 1
i = 10
while i > 0:
    print(i)
    i = i - 1

print('hemos terminado')



