a = input('digite el 1er valor ')
b = input('digite el 2º valor ')

if a == b:
    print(f'{a} y {b} son iguales') # esta lìnea está identada (tabulación)
    print(a,' y ',b,' son iguales')
if a < b:
    print(f'{a} es menor que {b}') # esta lìnea está identada (tabulación)

if a > b:
    print(f'{a} es mayor que {b}') # esta lìnea está identada (tabulación)



